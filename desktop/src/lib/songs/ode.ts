import { buildFromRows, type Row } from "./builder";

export function buildOdeToJoySong() {
  const q = 0.45, h = 0.9;
  const melody: Row[] = [
    [64, 0,    q*0.95], [64, q,   q*0.95], [65, 2*q, q*0.95], [67, 3*q, q*0.95],
    [67, 4*q, q*0.95], [65, 5*q, q*0.95], [64, 6*q, q*0.95], [62, 7*q, q*0.95],
    [60, 8*q, q*0.95], [60, 9*q, q*0.95], [62, 10*q,q*0.95], [64, 11*q,q*0.95],
    [64, 12*q,q*0.7],  [65, 13*q,q*0.7],  [65, 14*q,h*0.95],
    [64, 16*q,q*0.95], [64, 17*q,q*0.95], [65, 18*q,q*0.95], [67, 19*q,q*0.95],
    [67, 20*q,q*0.95], [65, 21*q,q*0.95], [64, 22*q,q*0.95], [62, 23*q,q*0.95],
    [60, 24*q,q*0.95], [62, 25*q,q*0.95], [64, 26*q,q*0.95], [62, 27*q,q*0.95],
    [60, 28*q,q*0.95], [62, 29*q,q*0.7],  [60, 30*q,h*0.95],
  ].map((r) => [r[0], r[1], r[2], 96, 0]) as Row[];
  const bass: Row[] = [
    [48, 0,  4*q*0.95, 60, 1], [55, 0,  4*q*0.95, 60, 1], [60, 0,  4*q*0.95, 60, 1],
    [53, 4*q,4*q*0.95, 60, 1], [60, 4*q,4*q*0.95, 60, 1], [65, 4*q,4*q*0.95, 60, 1],
    [55, 8*q,4*q*0.95, 60, 1], [59, 8*q,4*q*0.95, 60, 1], [62, 8*q,4*q*0.95, 60, 1],
    [48, 12*q,4*q*0.95,60, 1], [55, 12*q,4*q*0.95,60, 1], [60, 12*q,4*q*0.95,60, 1],
    [48, 16*q,4*q*0.95,60, 1], [55, 16*q,4*q*0.95,60, 1], [60, 16*q,4*q*0.95,60, 1],
    [53, 20*q,4*q*0.95,60, 1], [60, 20*q,4*q*0.95,60, 1], [65, 20*q,4*q*0.95,60, 1],
    [57, 24*q,4*q*0.95,60, 1], [60, 24*q,4*q*0.95,60, 1], [64, 24*q,4*q*0.95,60, 1],
    [48, 28*q,4*q*0.95,60, 1], [55, 28*q,4*q*0.95,60, 1], [60, 28*q,4*q*0.95,60, 1],
  ];
  return buildFromRows("Ode to Joy - Beethoven", melody.concat(bass));
}

import { buildFromRows, type Row } from "./builder";

export function buildFurEliseSong() {
  const s = 0.32;
  const e = s * 2;
  const m: Row[] = [
    [76,0,s],[75,s,s],[76,2*s,s],[75,3*s,s],[76,4*s,s],[71,5*s,s],[74,6*s,s],[72,7*s,e],
    [69,8*e,s],[60,8*e+s,s*0.95],[64,8*e+2*s,s],[69,8*e+3*s,e],
    [71,10*e,s*0.95],[64,10*e+s,s*0.95],[68,10*e+2*s,s*0.95],[71,10*e+3*s,e],
    [72,12*e,s*0.95],[64,12*e+s,s*0.95],[76,12*e+2*s,s],[75,12*e+3*s,s],
    [76,14*e,s],[75,15*e,s],[76,16*e,s],[71,17*e,s],[74,18*e,s],[72,19*e,e],
    [69,20*e,s],[60,20*e+s,s],[64,20*e+2*s,s],[69,20*e+3*s,e],
    [71,22*e,s],[64,22*e+s,s],[72,22*e+2*s,s*0.95],[71,22*e+3*s,e*1.4],
  ].map((r) => [r[0], r[1], (r[2] as number) * 0.95, 88, 0]) as Row[];
  const bass: Row[] = [
    [33, 0,    e, 56, 1],[45, 0,    e, 56, 1],
    [33, 2*e,  e, 56, 1],[45, 2*e,  e, 56, 1],
    [33, 4*e,  e, 56, 1],[45, 4*e,  e, 56, 1],
    [33, 6*e,  e, 56, 1],[45, 6*e,  e, 56, 1],
    [40, 8*e,  e, 56, 1],[52, 8*e,  e, 56, 1],
    [40, 10*e, e, 56, 1],[52, 10*e, e, 56, 1],
    [33, 12*e, e, 56, 1],[45, 12*e, e, 56, 1],
    [33, 14*e, e, 56, 1],[45, 14*e, e, 56, 1],
    [33, 16*e, e, 56, 1],[45, 16*e, e, 56, 1],
    [33, 18*e, e, 56, 1],[45, 18*e, e, 56, 1],
    [40, 20*e, e, 56, 1],[52, 20*e, e, 56, 1],
    [33, 22*e, e*2.5, 56, 1],[45, 22*e, e*2.5, 56, 1],
  ];
  return buildFromRows("Fur Elise (opening) - Beethoven", m.concat(bass));
}

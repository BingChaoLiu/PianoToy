import { buildFromRows, type Row } from "./builder";

export function buildMozartTwinkleVar() {
  const q = 0.4, h = 0.8, e = 0.2;
  
  // Theme: same as Twinkle but with Mozart's ornamentation
  const melody: Row[] = [
    // Theme bars 1-4
    [72,0,q],[72,q,q],[79,2*q,q],[79,3*q,q],[81,4*q,q],[81,5*q,q],[79,6*q,h],
    [77,8*q,q],[77,9*q,q],[76,10*q,q],[76,11*q,q],[74,12*q,q],[74,13*q,q],[72,14*q,h],
    // Theme bars 5-8
    [79,16*q,q],[79,17*q,q],[77,18*q,q],[77,19*q,q],[76,20*q,q],[76,21*q,q],[74,22*q,h],
    [79,24*q,q],[79,25*q,q],[77,26*q,q],[77,27*q,q],[76,28*q,q],[76,29*q,q],[74,30*q,h],
    // Theme bars 9-12
    [72,32*q,q],[72,33*q,q],[79,34*q,q],[79,35*q,q],[81,36*q,q],[81,37*q,q],[79,38*q,h],
    [77,40*q,q],[77,41*q,q],[76,42*q,q],[76,43*q,q],[74,44*q,q],[74,45*q,q],[72,46*q,h],
    // Variation 1: eighth-note runs
    [72,48*q,e],[74,49*e,e],[72,49.5*e,e],[74,50*e,e],[79,50.5*e,e],[81,51*e,e],[79,51.5*e,e],[81,52*e,e],
    [83,52.5*e,e],[81,53*e,e],[83,53.5*e,e],[84,54*e,q],[84,54.5*e,e],[83,55*e,e],[81,55.5*e,e],[79,56*e,h],
    // Var cont
    [77,56+q,e],[79,56.5+q,e],[77,56.75+q,e],[79,57,e],[76,57.25,e],[77,57.5,e],[76,57.75,e],[74,58,e],
    [76,58.25,e],[74,58.5,e],[72,58.75,e],[74,59,e],[72,59.25,e],[71,59.5,e],[72,59.75,e],[72,60,e],
    [72,60.25,e],[74,60.5,e],[72,60.75,e],[74,61,e],[79,61.25,e],[81,61.5,e],[79,61.75,e],[81,62,e],
    [83,62.25,e],[84,62.5,e],[83,62.75,e],[84,63,e,q],[86,63.5,e],[84,64,e,q],
  ].map((r) => [r[0], r[1], (r[2] as number) * 0.88, 85, 0]) as Row[];

  const bass: Row[] = [];
  const chords = [
    [48,60,67],[48,60,67],[53,60,65],[53,60,65],
    [55,62,67],[55,62,67],[48,60,67],[48,60,67],
    [55,62,67],[55,62,67],[53,60,65],[53,60,65],
    [55,62,67],[55,62,67],[48,60,67],[48,60,67],
  ];
  chords.forEach((c, i) => c.forEach((m) => bass.push([m, i * 2.4, 2.16, 55, 1])));

  return buildFromRows("Twinkle Variations (K.265) - Mozart", melody.concat(bass));
}

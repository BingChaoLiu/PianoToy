import { buildFromRows, type Row } from "./builder";

export function buildBeethovenSonatinaG() {
  const q = 0.35, h = 0.7, e = 0.175;
  
  const melody: Row[] = [
    // Opening theme (bars 1-4)
    [67,0,q],[71,q,q],[74,2*q,q],[79,3*q,q],[77,4*q,h],[74,5*q,q],
    [72,5.5*q,e],[74,5.75*q,e],[77,6*q,h],
    // Bars 5-8
    [79,7*q,q],[77,7.5*q,e],[76,7.75*q,e],[74,8*q,q],[76,8.5*q,e],[77,8.75*q,e],
    [79,9*q,q],[81,9.5*q,e],[79,10*q,q],[77,10.5*q,e],[76,11*q,q],[74,11.5*q,e],
    [72,12*q,h],[67,13*q,q],[71,13.5*q,q],
    // Bars 9-12
    [74,14*q,q],[79,14.5*q,q],[77,15*q,h],[74,15.5*q,e],
    [76,16*q,q],[74,16.5*q,e],[72,17*q,q],[71,17.5*q,e],
    [67,18*q,q],[71,18.5*q,q],[74,19*q,q],[72,19.5*q,e],[71,20*q,h],
    [67,20.5*q,q],[66,21*q,q],[67,21.5*q,q],
    // Bars 13-16
    [71,22*q,q],[74,22.5*q,q],[77,23*q,q],[79,23.5*q,q],
    [77,24*q,h],[74,24.5*q,q],[72,25*q,h],
    [67,26*q,q],[69,26.5*q,q],[71,27*q,q],[69,27.5*q,e],[67,28*q,h],
    // Bars 17-20 recapitulation
    [67,29*q,q],[71,29.5*q,q],[74,30*q,q],[79,30.5*q,q],[77,31*q,h],[74,31.5*q,q],
    [72,32*q,e],[74,32.25*q,e],[77,32.5*q,h],
    [79,33*q,q],[77,33.5*q,e],[76,33.75*q,e],[74,34*q,q],[72,34.5*q,q],
    [67,35*q,h],
  ].map((r) => [r[0], r[1], (r[2] as number) * 0.88, 85, 0]) as Row[];

  const bass: Row[] = [];
  const bassPattern = [
    [43,55,59],[43,55,59],[47,55,62],[47,55,62],
    [50,57,62],[50,57,62],[43,55,59],[43,55,59],
    [47,55,62],[47,55,62],[43,55,59],[43,55,59],
    [47,55,62],[50,57,62],[43,55,59],[43,55,59],
    [43,55,59],[43,55,59],[47,55,62],[47,55,62],
  ];
  bassPattern.forEach((c, i) => c.forEach((m) => bass.push([m, i * 1.75, 1.58, 55, 1])));

  return buildFromRows("Sonatina in G (Anh.5) - Beethoven", melody.concat(bass));
}
